def create_thumbnail(
    course_name:  str,
    unit_name:    str,
    output_path:  str = "thumbnail_out.jpg",
) -> str:
    """Generate thumbnail using the embedded template image."""
    img  = _load_template()
    draw = ImageDraw.Draw(img)
    W, H = img.size

    scale = W / 800.0

    safe_top, safe_bottom, cx, safe_width = find_light_circle_zone(img)
    zone_h    = safe_bottom - safe_top
    inter_gap = max(28, int(H * 0.06))

    # ── Course name: extrabold, PREFER 1 LINE ──
    # Try 1 line first, shrink font down to min_1line size.
    # Only allow 2 lines if text can't fit on 1 line at reasonable size.
    max_course_size = int(42 * scale)
    min_1line_size  = int(18 * scale)          # below this → allow 2 lines

    # Binary search helper: find largest sz in [lo, hi] that fits
    def _best_font_size(lo, hi, max_lines, max_h_frac):
        best_sz, best_cl, best_cw, best_ch, best_font = None, None, None, None, None
        while lo <= hi:
            mid = (lo + hi) // 2
            f = load_font("extrabold", mid)
            lines, ws, hs = wrap_to_fit(course_name, f, safe_width, max_lines=max_lines)
            fits = (
                len(lines) <= max_lines
                and max(ws) <= safe_width
                and block_height(hs, line_gap=8) <= zone_h * max_h_frac
            )
            if fits:
                best_sz, best_cl, best_cw, best_ch, best_font = mid, lines, ws, hs, f
                lo = mid + 1        # try larger
            else:
                hi = mid - 1        # too big
        return best_sz, best_cl, best_cw, best_ch, best_font

    # Attempt 1: fit on 1 line
    eb_size, cl, cw, ch, eb_font = _best_font_size(
        min_1line_size, max_course_size, max_lines=1, max_h_frac=0.45
    )
    found_1line = eb_size is not None

    if not found_1line:
        # Attempt 2: couldn't fit on 1 line → allow 2 lines
        eb_size, cl, cw, ch, eb_font = _best_font_size(
            10, max_course_size, max_lines=2, max_h_frac=0.45
        )
        if eb_size is None:
            # fallback
            eb_size = 10
            eb_font = load_font("extrabold", eb_size)
            cl, cw, ch = wrap_to_fit(course_name, eb_font, safe_width, max_lines=2)

    # ── Unit name: medium weight, ~55% of course size ──
    med_size = max(20, int(eb_size * 0.68))
    med_font = load_font("medium", med_size)
    unl, unw, unh = wrap_to_fit(unit_name, med_font, int(safe_width * 0.85))

    total_h = block_height(ch, 8) + inter_gap + block_height(unh, 6)
    y = safe_top + (zone_h - total_h) // 2

    y = draw_block_centred(draw, cl,  cw,  ch,  eb_font, cx, y, WHITE, line_gap=8)
    y += inter_gap
    draw_block_centred(draw, unl, unw, unh, med_font, cx, y, WHITE, line_gap=6)

    # Resize to 1920x1080 (Full HD, even dimensions for h264)
    img = img.resize((1920, 1080), Image.LANCZOS)

    img.save(output_path, quality=95)
    return output_path


