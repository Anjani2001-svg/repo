# TEXT LAYOUT HELPERS
# ─────────────────────────────────────────────────────────────────

def wrap_to_fit(text: str, font, max_px: int, max_lines: int = 0):
    """Word-wrap text so no line exceeds max_px.
       If max_lines > 0, never exceed that many lines (shrink font if needed).
       Uses binary search instead of linear scan for speed.
    """
    text = text.strip()
    if not text:
        bb = font.getbbox(" ")
        return [""], [0], [bb[3] - bb[1]]

    def _try_wrap(chars):
        lines = textwrap.wrap(text, width=chars) or [text]
        if max_lines > 0 and len(lines) > max_lines:
            return None
        ws, hs = [], []
        for line in lines:
            bb = font.getbbox(line)
            ws.append(bb[2] - bb[0])
            hs.append(bb[3] - bb[1])
        if max(ws) <= max_px:
            return lines, ws, hs
        return None

    # Binary search: find smallest chars value that fits
    lo, hi = 4, len(text)
    best = None

    # First check if full text fits on one line
    result = _try_wrap(hi)
    if result:
        return result

    while lo <= hi:
        mid = (lo + hi) // 2
        result = _try_wrap(mid)
        if result:
            best = result
            # Try fewer chars (more lines) — but we want widest fit, so go higher
            lo = mid + 1
        else:
            hi = mid - 1

    if best:
        return best

    bb = font.getbbox(text)
    return [text], [bb[2] - bb[0]], [bb[3] - bb[1]]


def block_height(heights: list, line_gap: int = 6) -> int:
    return sum(heights) + line_gap * (len(heights) - 1)


def draw_block_centred(draw, lines, widths, heights, font,
                       cx: int, top_y: int, fill, line_gap: int = 6):
    y = top_y
    for i, line in enumerate(lines):
        x = cx - widths[i] // 2
        draw.text((x, y), line, font=font, fill=fill)
        y += heights[i] + line_gap
    return y


