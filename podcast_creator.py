def _ffprobe_duration_seconds(audio_path: str) -> float:
    cmd = [
        "ffprobe", "-v", "error",
        "-show_entries", "format=duration",
        "-of", "default=noprint_wrappers=1:nokey=1",
        audio_path,
    ]
    r = subprocess.run(cmd, capture_output=True, text=True)
    if r.returncode != 0:
        raise RuntimeError(f"ffprobe error:\n{r.stderr}")
    try:
        return float(r.stdout.strip())
    except ValueError:
        raise RuntimeError(f"Could not parse duration from ffprobe:\n{r.stdout}")


def create_video(
    image_path: str,
    audio_path: str,
    output_path: str,
    progress_cb: Optional[Callable[[int, str], None]] = None,
) -> str:
    import shutil
    if not shutil.which("ffmpeg"):
        raise RuntimeError("ffmpeg not found. Install it first.")
    if not shutil.which("ffprobe"):
        raise RuntimeError("ffprobe not found (should come with ffmpeg).")

    duration = _ffprobe_duration_seconds(audio_path)
    if duration <= 0:
        duration = 1.0

    audio_ext = Path(audio_path).suffix.lower()
    audio_codec = ["-c:a", "copy"] if audio_ext in (".aac", ".m4a") else ["-c:a", "aac", "-b:a", "192k"]

    cmd = [
        "ffmpeg", "-y",
        "-loop", "1", "-framerate", "1", "-i", image_path,
        "-i", audio_path,
        "-vf", "scale=trunc(iw/2)*2:trunc(ih/2)*2",
        "-c:v", "libx264", "-preset", "ultrafast",
        "-tune", "stillimage", "-crf", "28", "-r", "1",
        *audio_codec,
        "-pix_fmt", "yuv420p", "-shortest",
        "-threads", "0",
        "-movflags", "+faststart",
        "-progress", "pipe:1", "-nostats",
        output_path,
    ]

    p = subprocess.Popen(cmd, stdout=subprocess.PIPE, stderr=subprocess.PIPE,
                         text=True, bufsize=1, universal_newlines=True)

    last_percent = -1
    if progress_cb:
        progress_cb(0, "Rendering: 0%")

    try:
        while True:
            line = p.stdout.readline()
            if not line:
                if p.poll() is not None:
                    break
                continue
            line = line.strip()
            if "=" not in line:
                continue
            key, val = line.split("=", 1)
            if key in ("out_time_ms", "out_time_us"):
                try:
                    t = int(val)
                except ValueError:
                    continue
                seconds = t / 1_000_000.0
                percent = int(min(99, (seconds / duration) * 100))
                if percent != last_percent:
                    last_percent = percent
                    if progress_cb:
                        progress_cb(percent, f"Rendering: {percent}%")
            elif key == "progress" and val == "end":
                if progress_cb:
                    progress_cb(100, "Rendering: 100%")
                break

        rc = p.wait()
        if rc != 0:
            err = (p.stderr.read() or "").strip()
            raise RuntimeError(f"ffmpeg error:\n{err}")
    finally:
        try:
            if p.stdout: p.stdout.close()
            if p.stderr: p.stderr.close()
        except Exception:
            pass

    return output_path


